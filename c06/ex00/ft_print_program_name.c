#include <unistd.h>

int	main(int argc, char *argv[])
{
	char	*tab;

	tab = argv[0];
	while (*tab)
	{
		write(1, &*tab, 1);
		tab++;
	}
	write(1, "\n", 1);
	return (argc);
}
